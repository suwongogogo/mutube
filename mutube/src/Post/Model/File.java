package Post.Model;

public class File {
	String fileName;
	String fileRealName;
	
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public void setFileRealName(String fileRealName) {
		this.fileRealName = fileRealName;
	}
	public String getFileName() {
		return fileName;
	}
	public String getFileRealName() {
		return fileRealName;
	}
	
}
